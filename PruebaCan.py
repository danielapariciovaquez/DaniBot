import serial
import time

ser = serial.Serial("/dev/ttyUSB0", 2000000, timeout=1)

# ENABLE
ser.write(bytes([
    0xAA, 0x83,
    0x01, 0x00,
    0xF3, 0x01, 0xF5
]))

time.sleep(0.1)

# RUN 100 RPM
ser.write(bytes([
    0xAA, 0x85,
    0x01, 0x00,
    0xF6, 0x80, 0x64, 0x0A, 0xE5
]))

ser.close()
